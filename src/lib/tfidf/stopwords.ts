/**
 * 주어진 토큰이 불용어인지 확인합니다.
 * @param token 확인할 토큰 (소문자로 변환되어야 함)
 * @returns 불용어 여부
 */
export function isStopword(token: string): boolean {
  const lowerToken = token.toLowerCase();
  return KOREAN_STOPWORDS.has(lowerToken) || ENGLISH_STOPWORDS.has(lowerToken);
}
